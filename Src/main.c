void Tim_Delay_us (uint16_t us) //--------- Tim_Delay_us(10); -----------10µs Delay
{
	__HAL_TIM_SET_COUNTER(&htim6,0);  // set the counter value a 0
	while (__HAL_TIM_GET_COUNTER(&htim6) < us);  // wait for the counter to reach the us input in the parameter
}

uint32_t Read_HCS04()
{
	uint32_t local_time = 0;

	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_SET);
	Tim_Delay_us(10);

	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET);

	while(!HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_7));

	while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_7))
	{
		local_time ++;
		Tim_Delay_us(1);
	}

	return local_time;

}


